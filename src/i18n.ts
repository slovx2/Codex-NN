export type TranslationKey = keyof typeof en;

let language: ResolvedLanguage = "zh-CN";

export function setResolvedLanguage(value: ResolvedLanguage): void {
  language = value;
  document.documentElement.lang = value;
  document.title = value === "en" ? "Codex NN" : "Codex 暖暖";
}

export function resolvedLanguage(): ResolvedLanguage {
  return language;
}

export function currentSystemLocale(): string {
  return navigator.languages?.[0] ?? navigator.language ?? "en";
}

export function resolveSystemLanguage(systemLocale: string): ResolvedLanguage {
  return systemLocale.trim().toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function t(key: TranslationKey, values: Record<string, string | number> = {}): string {
  const template = language === "en" ? en[key] : zh[key];
  return Object.entries(values).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template
  );
}

