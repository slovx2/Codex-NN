export type MarketplaceTranslationKey = keyof typeof messages;

export function mt(
  key: MarketplaceTranslationKey,
  values: Record<string, string | number> = {}
): string {
  let result: string = messages[key][resolvedLanguage() === "en" ? 1 : 0];
  for (const [name, value] of Object.entries(values)) {
    result = result.replaceAll(`{${name}}`, String(value));
  }
  return result;
}
